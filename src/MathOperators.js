export function doAddition(firstNum, secondNum) {
  return firstNum + secondNum;
}

export function doSubtraction(firstNum, secondNum) {
  return firstNum - secondNum;
}

export function doMultiplication(firstNum, secondNum) {
  return firstNum * secondNum;
}

export function doDivision(firstNum, secondNum) {
  return firstNum / secondNum;
}
